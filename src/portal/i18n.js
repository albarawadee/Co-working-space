/**
 * Wrap a base `t()` (from PortalChrome) so v2 keys resolve to V2 dict,
 * and all other keys fall through to the existing translation.
 * Use: `const t = wrapT(baseT, lang)` in v2 screens.
 */
export function wrapT(baseT, lang) {
  const dict = V2[lang] || V2.en;
  return function tV2(key, vars) {
    if (key in dict) {
      let str = dict[key];
      if (vars) {
        for (const k in vars) str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), vars[k]);
      }
      return str;
    }
    return baseT(key, vars);
  };
}
